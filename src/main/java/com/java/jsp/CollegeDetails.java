package com.java.jsp;

public class CollegeDetails {
	
	private int college_id;
	private String college_Name;
	private int college_code;
	private int college_pinCode;
	
	
	public int getCollege_id() {
		return college_id;
	}
	public void setCollege_id(int college_id) {
		this.college_id = college_id;
	}
	public String getCollege_Name() {
		return college_Name;
	}
	public void setCollege_Name(String college_Name) {
		this.college_Name = college_Name;
	}
	public int getCollege_code() {
		return college_code;
	}
	public void setCollege_code(int college_code) {
		this.college_code = college_code;
	}
	public int getCollege_pinCode() {
		return college_pinCode;
	}
	public void setCollege_pinCode(int college_pinCode) {
		this.college_pinCode = college_pinCode;
	}
	
	
	
	

}
