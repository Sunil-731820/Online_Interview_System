package com.java.jsp;

public class ProgrammingDetails {
	private int programming_id;
	private String firstProgramming;
	private String secondProgramming;
	private String thirdProgramming;
	private String fourthProgramming;
	
	
	public int getProgramming_id() {
		return programming_id;
	}
	public void setProgramming_id(int programming_id) {
		this.programming_id = programming_id;
	}
	public String getFirstProgramming() {
		return firstProgramming;
	}
	public void setFirstProgramming(String firstProgramming) {
		this.firstProgramming = firstProgramming;
	}
	public String getSecondProgramming() {
		return secondProgramming;
	}
	public void setSecondProgramming(String secondProgramming) {
		this.secondProgramming = secondProgramming;
	}
	public String getThirdProgramming() {
		return thirdProgramming;
	}
	public void setThirdProgramming(String thirdProgramming) {
		this.thirdProgramming = thirdProgramming;
	}
	public String getFourthProgramming() {
		return fourthProgramming;
	}
	public void setFourthProgramming(String fourthProgramming) {
		this.fourthProgramming = fourthProgramming;
	}
	
	
	

}
